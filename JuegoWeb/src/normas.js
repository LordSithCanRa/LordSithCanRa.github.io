function obtenerNormas(string){
    let html;
    switch (string) {
        case "pinturillo":
            html = pinturillo;
            break;
    
        case "quePrefieres":
            html = quePrefieres
            break;

        case "yoNunca":
            html = yoNunca;
            break;

        case "masProbable":
            html = masProbable;
            break;

        case "dueloMiradas":
            html = dueloMiradas;
            break;
        case "carrerita":
            html = carrerita;
            break;
        case "verdaderoVSfalso":
            html = verdaderoVSfalso;
            break;
        case "mayorOmenor":
            html = mayorOmenor;
            break;
        case "cascada":
            html = cascada;
            break;
        case "arcoiris":
            html = arcoiris;
            break;
    }
    return html;
}

function alertaNormas(string){
    Swal.fire({
        title: `<i class="fa-solid fa-book-skull"></i><strong>  NORMAS  </strong><i class="fa-solid fa-book-skull"></i>`,
        html: obtenerNormas(string),
        showCloseButton: false,
        showCancelButton: true,
        showConfirmButton: false,
        confirmButtonAriaLabel: "Thumbs up, great!",
        cancelButtonText: `
            Acepto
        `,
        cancelButtonAriaLabel: "Thumbs down"
        });
}
